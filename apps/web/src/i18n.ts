export const STRINGS: Record<string, Dict> = { en: EN, ru: RU };

export function useT(): Dict {
  const { settings } = useApp();
  return STRINGS[settings.lang] ?? EN;
}

export function timeAgoText(ts: number, t: Dict) {
  const m = Math.round((Date.now() - ts) / 60000);
  if (m < 1) return t.justNow;
  if (m < 60) return t.minAgo(m);
  const h = Math.round(m / 60);
  if (h < 24) return t.hAgo(h);
  const d = Math.round(h / 24);
  return d === 1 ? t.yesterday : t.daysAgo(d);
}
